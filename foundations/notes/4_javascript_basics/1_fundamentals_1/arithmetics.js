
// Everything is pretty much the same as in python.
let x = 5;
let y = 2;
let z = x / y;
console.log(z);

let k = (100 + 50) * 3;
console.log(k) // 450



// Incrementing, Decrementing
let num = 5;
num++;
console.log(num); // 6

num--;
num--;
console.log(num); // 4

let b = 7 * num;
console.log(b);

// Just as in Python, there are syntax sugars like:
let myNum = 3;
myNum += 2;
myNum **= 3;
console.log(myNum); // 125
